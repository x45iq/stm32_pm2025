#ifndef SSD1306_H
#define SSD1306_H

#include <stdint.h>

#define SSD1306_WIDTH 128
#define SSD1306_HEIGHT 64

#define SSD1306_BUFFER_SIZE (SSD1306_WIDTH * SSD1306_HEIGHT / 8)

#define SSD1306_SETCONTRAST 0x81
#define SSD1306_DISPLAYALLON_RESUME 0xA4
#define SSD1306_DISPLAYALLON 0xA5
#define SSD1306_NORMALDISPLAY 0xA6
#define SSD1306_INVERTDISPLAY 0xA7
#define SSD1306_DISPLAYOFF 0xAE
#define SSD1306_DISPLAYON 0xAF
#define SSD1306_SETDISPLAYOFFSET 0xD3
#define SSD1306_SETCOMPINS 0xDA
#define SSD1306_SETVCOMDETECT 0xDB
#define SSD1306_SETDISPLAYCLOCKDIV 0xD5
#define SSD1306_SETPRECHARGE 0xD9
#define SSD1306_SETMULTIPLEX 0xA8
#define SSD1306_SETLOWCOLUMN 0x00
#define SSD1306_SETHIGHCOLUMN 0x10
#define SSD1306_SETSTARTLINE 0x40
#define SSD1306_MEMORYMODE 0x20
#define SSD1306_COLUMNADDR 0x21
#define SSD1306_PAGEADDR 0x22
#define SSD1306_COMSCANINC 0xC0
#define SSD1306_COMSCANDEC 0xC8
#define SSD1306_SEGREMAP 0xA0
#define SSD1306_CHARGEPUMP 0x8D

#define SSD1306_DC_PORT GPIOB
#define SSD1306_DC_PIN GPIO_ODR_ODR0
#define SSD1306_RST_PORT GPIOB
#define SSD1306_RST_PIN GPIO_ODR_ODR1
#define SSD1306_CS_PORT GPIOA
#define SSD1306_CS_PIN GPIO_ODR_ODR4

#define SSD1306_CS_LOW() (SSD1306_CS_PORT->BSRR = (SSD1306_CS_PIN << 16))
#define SSD1306_CS_HIGH() (SSD1306_CS_PORT->BSRR = SSD1306_CS_PIN)
#define SSD1306_DC_LOW() (SSD1306_DC_PORT->BSRR = (SSD1306_DC_PIN << 16))
#define SSD1306_DC_HIGH() (SSD1306_DC_PORT->BSRR = SSD1306_DC_PIN)
#define SSD1306_RST_LOW() (SSD1306_RST_PORT->BSRR = (SSD1306_RST_PIN << 16))
#define SSD1306_RST_HIGH() (SSD1306_RST_PORT->BSRR = SSD1306_RST_PIN)

void SSD1306_Init(void);
void SSD1306_WriteCommand(uint8_t cmd);
void SSD1306_WriteData(uint8_t data);
void SSD1306_UpdateScreen(void);
void SSD1306_Fill(uint8_t color);
void SSD1306_SetPixel(uint8_t x, uint8_t y, uint8_t color);
void SSD1306_DrawChessboard(uint8_t square_size);

extern uint8_t ssd1306_buffer[SSD1306_BUFFER_SIZE];

#endif
